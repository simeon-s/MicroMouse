/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */

  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */

	static unsigned short usDelta = 0;
	uint32_t scan_mask;
	unsigned char key_pos;
	uint32_t kbd_port;

	//------------------ 0.1 ms  tasks -----------------
	// countdown timer
	if (gulTickCountDown)
		gulTickCountDown--;

	if (gulSysTimer)
		gulSysTimer--;

	gulTimeTick++;


#ifdef IR_NEC_PROTOCOL
	IR_TimerHandler();
#endif

	//------------------ 10 ms  tasks -----------------
	if (++usDelta == 100) {	// update g_ulSysTickCount 100 times per second
		usDelta = 0;
		/*
		 //--- Speed Calculation -----
		 enc_left_speed = enc_leftCounter - enc_last_leftCounter;
		 enc_last_leftCounter = enc_leftCounter;

		 enc_right_speed = enc_rightCounter - enc_last_rightCounter;
		 enc_last_rightCounter = enc_rightCounter;
		 */
		diff = errorX - lastErrorX;
		lastErrorX = errorX;

		//--------- Keyboard ----------
		if (gusKeyRepeatTimer) {
			if (--gusKeyRepeatTimer == 0)
				gucRepeatOff = 1;
		}

		if (!gucKeyboardTimer) {
			kbd_port = ~(GPIOB->IDR & 0x407);
			kbd_port &= 0x407;
			if (kbd_port != 0x00) {
				scan_mask = 0x01;
				key_pos = 1;
				while (scan_mask & 0x7FF) {
					if (kbd_port & scan_mask) {
						break;
					}
					key_pos++;
					scan_mask <<= 1;
				}
				if (scan_mask & 0x7FF)
					gucKeyboardKey = key_pos;
			} else {
				gucKeyboardKey = 0xFF;
			}
		}

		if (gucKeyboardTimer)
			gucKeyboardTimer--;
		//----------------------------
	}

  /* USER CODE END SysTick_IRQn 1 */
}

